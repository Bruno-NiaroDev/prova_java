
public class Aluno {

    String nome;
    String curso;
    int serie;
    String disciplina;
    Double avaliacao;

    public  Aluno (String nome, String curso, int serie, String disciplina, double avaliacao){
        this.nome = nome;
        this.curso = curso;
        this.serie = serie;
        this.disciplina = disciplina;
        this.avaliacao = avaliacao;
    }
}
